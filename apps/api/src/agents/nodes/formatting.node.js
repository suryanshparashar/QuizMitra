// apps/api/src/agents/nodes/formatting.node.js

export const formattingNode = async (state) => {
  const { verifiedQuestions, requirements } = state;

  // Transform into the schema expected by the database/controller
  const finalQuestions = verifiedQuestions.map(q => ({
    questionText: q.questionText,
    options: q.options,
    correctAnswer: q.correctAnswer,
    // correctOptions: [], // explicitly empty or undefined for multiple-choice
    questionType: "multiple-choice", // Defaulting for MVP
    points: requirements.marksPerQuestion || 1,
    difficulty: q.difficulty || requirements.difficultyLevel || "medium",
    topic: q.topic || "General",
    explanation: "Generated by QuizMitra AI"
  }));

  return { 
      verifiedQuestions: finalQuestions,
      status: "completed" 
  };
};
